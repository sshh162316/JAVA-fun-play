class Wrapp3{
    //make use of character wrapper class method
    public static void main (String []args){
        Character ch = new Character('A');

        System.out.println("Character: " + Character.isLetter(ch));
        System.out.println("Character: " + Character.isDigit(ch));
        System.out.println("Character: " + Character.isUpperCase(ch));
        System.out.println("Character: " + Character.isLowerCase(ch));
        System.out.println("Character: " + Character.toLowerCase(ch));
        System.out.println("Character: " + Character.toUpperCase(ch));
        System.out.println("Character: " + Character.isWhitespace(ch));





    }
}